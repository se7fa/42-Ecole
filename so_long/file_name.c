#include "so_long.h"

int	ft_check_file_name(char *str)
{
	int	len;

	len = ft_strlen(str);
	if (str[len - 1] != 'r' && str[len - 2] != 'e' && str[len - 3] != 'b'\
			&& str[len - 4] != '.' )
		return (0);
	return (1);
}
